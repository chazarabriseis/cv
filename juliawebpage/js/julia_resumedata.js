function displayPatents(patents) {
	for (item in patents) {
	$('#patent').append(HTMLpatentStart);
	if (julia.patents[item].website) {
		var formattedTitlecom = HTMLpatentTitleWeb.replace("%href%",julia.patents[item].website);
		$('.patent-entry:last').append(asign(formattedTitlecom,julia.patents[item].title));
		} else {
		$('.patent-entry:last').append(asign(HTMLpatentTitle,julia.patents[item].title));	
		}
	$('.patent-entry:last').append(asign(HTMLpatentAuthors,julia.patents[item].authors));
	if (julia.patents[item].journal) {
		$('.patent-entry:last').append(asign(HTMLpatentJournal,julia.patents[item].journal));
		} else {
		formattedDocket = HTMLpatentDocket.replace('%location%',julia.patents[item].location);
		$('.patent-entry:last').append(asign(formattedDocket,julia.patents[item].docket));
		}
	$('.patent-entry:last').append(asign(HTMLpatentDates,julia.patents[item].date));
	$('.patent-entry:last').append(asign(HTMLpatentDescription,julia.patents[item].summary));
	$('.patent-entry:last').append(asign(HTMLpatentImage,julia.patents[item].image));
	}
}
displayPatents(julia.patents)

